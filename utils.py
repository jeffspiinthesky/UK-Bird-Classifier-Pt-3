import cv2
import numpy as np


MARGIN = 10  # pixels
ROW_SIZE = 30  # pixels
FONT_SIZE = 1
FONT_THICKNESS = 1
TEXT_COLOR = (0, 0, 0)  # black
#FONT_STYLE = cv2.FONT_HERSHEY_DUPLEX
FONT_STYLE = cv2.FONT_HERSHEY_PLAIN

def draw_text(img, text,
          font=FONT_STYLE,
          pos=(0, 0),
          font_scale=3,
          font_thickness=2,
          text_color=(0, 255, 0),
          text_color_bg=(0, 0, 0)
          ):

    x, y = pos
    text_size, _ = cv2.getTextSize(text, font, font_scale, font_thickness)
    text_w, text_h = text_size
    cv2.rectangle(img, pos, (x + text_w+2, y + text_h+2), text_color_bg, -1)
    cv2.putText(img, text, (x, y + text_h + font_scale - 1), font, font_scale, text_color, font_thickness)

    return text_size

def visualize(
    image,
    detection_result
) -> np.ndarray:
  """Draws bounding boxes on the input image and return it.
  Args:
    image: The input RGB image.
    detection_result: The list of all "Detection" entities to be visualized.
  Returns:
    Image with bounding boxes.
  """
  for detection in detection_result.detections:
    # Draw bounding_box
    bbox = detection.bounding_box
    start_point = bbox.origin_x, bbox.origin_y
    end_point = bbox.origin_x + bbox.width, bbox.origin_y + bbox.height
    # Use the orange color for high visibility.
    cv2.rectangle(image, start_point, end_point, (0, 255, 255), 2)

    # Draw label and score
    category = detection.categories[0]
    category_name = category.category_name
    probability = round(category.score, 2)
    result_text = category_name + ' (' + str(probability) + ')'
    text_location = (bbox.origin_x,
                     bbox.origin_y-10)
    #cv2.putText(image, result_text, text_location, FONT_STYLE,
    #            FONT_SIZE, TEXT_COLOR, FONT_THICKNESS, cv2.LINE_AA)
    draw_text(image, result_text, FONT_STYLE, text_location, font_scale=FONT_SIZE, font_thickness=FONT_THICKNESS, text_color=TEXT_COLOR, text_color_bg=(0,255,255))

  return image
